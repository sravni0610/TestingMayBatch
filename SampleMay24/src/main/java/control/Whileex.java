package control;

public class Whileex {

    public static void main(String args[]){

        int i=11;

        int j =11;

        while(i<=10){

            System.out.println(i);
            i++;
        }

        System.out.println("Entering into Do while");

        do{
            System.out.println(j);
            j++;
        }while(j<=10);
    }
}
